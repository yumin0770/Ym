SELECT EMP_NAME, RPAD(SUBSTR(EMP_NO,1,8) ,13, '*') "주민등록번호",
	   REPLACE(EMP_NO, SUBSTR(EMP_NO,9), '******') "주민등록번호",
       SUBSTR(EMP_NO,1,8)||'******' "주민등록번호" 
FROM EMPLOYEE ;



--5번
SELECT EMP_NAME,JOB_CODE,TO_CHAR(NVL((SALARY+(SALARY*BONUS))*12,SALARY*12)
						,'L999,999,999')"연봉(원)",
	  TO_CHAR((SALARY+(SALARY*NVL(BONUS,0))*12)
						,'L999,999,999')"연봉(원)"
FROM EMPLOYEE;

