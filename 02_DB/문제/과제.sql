--WHERE DEPT_CODE IN ('D5','D9)
--AND EXTRACT(YEAR FROM HIRE_DATE) = '2004'
AND TO_CHAR(HIRE_DATE, 'YYYY') = '2004';

--7번
SELECT EMP_NAME,HIRE_DATE
	   ,EXTRACT (DAY FROM (LAST_DAY(HIRE_DATE)
       -EXTRACT (DAY FROM HIRE_DATE)+1)) "입사한 달의 근무"
       ,LAST_DAY(HIRE_DATE)-HIRE_DATE+1 "입사한 달의 근무"
FROM EMPLOYEE;

